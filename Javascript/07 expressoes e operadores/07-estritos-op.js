// Operadores de comparação

// Irá comparar valores e retornar um boolean como resposta da comparação

let one = 1
let two = 2

// === estritamente iguais (iguais e do mesmo tipo: compara string e valor)
// != estritamente diferente de 

console.log(one === '1');
console.log(one === 1);

console.log(two !== '2');
console.log(two !== 2);